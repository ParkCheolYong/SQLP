인덱스 변경하지 않고 힌트지정 불가 조건에서 테이블이 배타적 관계일때

--인덱스 구성
--작업지시_X1 : 방문예정일자


SELECT A.작업일련번호
     , A.작업자ID
	 , DECODE(A.작업구분코드, 'A', '개통', 'B', '장애') 작업구분
	 , DECODE(A.작업구분코드, 'A', B.고객번호, 'B', C.고객번호) 고객번호
	 , DECODE(A.작업구분코드, 'A', B.주소, 'B', C.주소) 주소
  FROM 작업지시 A, 개통접수 B, 장애접수 C
 WHERE A.방문예정일자 = TO_CHAR(SYSDATE, 'yyyymmdd')   
   AND B.개통접수번호(+) = DECODE(A.작업구분코드, 'A', A.접수번호)
   AND C.장애접수번호(+) = DECODE(A.작업구분코드, 'B', A.접수번호)